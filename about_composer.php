						<h1>About Composer</h1>
						<p>Sree Subrahmanya Bhajanaamrutham was composed by our great grand father late. A.Raama Saasthri of Vadiveeswaram Gramam which now forms part of Naagercoil Town in Kanyakumari District of Tamil Nadu. It seems that his ancestors had migrated from Chidambaram-Vaideeswarancoil-Seerkazhi belt of the kaveri delta 300 to 400 years ago to the princely state of Travancore.</p>
						<p>During the short span of 33 years(1840-1873) he had composed these Bhajana Paddhathi and quite a number of other musical compositions. We could lay our hands only on a few of his compositions</p>
						<p>Sree Rama Saasthri was also mainly responsible for the institution of the <a href="https://www.sringeri.net/branches/tamil-nadu/nagercoil" target="_blank">Subrahmanya Swami Bhajana Matam</a> with the assistance of the residence of the locality. The musically inclined devotees took active part in the Bhajans rendered daily in the matam. This legacy has been handed over to us down the line and the bhajans are conducted even now. During the <a href="https://en.wikipedia.org/wiki/Shashthi_(day)" target="_blank">Shashti</a> and <a href="http://murugan.org/research/duraiswamy-vaikasi-visakam.htm" target="_blank">Vaikasi Visakam</a> Festivals the bhajans are rendered more elaborately.</p>
						<p>The composer was a great scholar in Sanskrit and so the composition too are in Sanskrit. He was an ardent devotee of Lord Sthaanu Natha, the presiding deity of <a href="https://en.wikipedia.org/wiki/Thanumalayan_Temple" target="_blank">Suchindram Temple</a>. The composition bears the mudra “STHAANUNATHA”.</p>
						<p>The notes for the keerthanaas given in this website are by Smt.R.Ramani, one of the great grand daughters of the composer and a retired Assistant Professor of Carnatic Music of the <a href="https://en.wikipedia.org/wiki/Chembai_Memorial_Government_Music_College" target="_blank"> Chembai Music College, Palakkad</a>. She has mostly adhered to the pattern of rendering that has come down to us.</p>
